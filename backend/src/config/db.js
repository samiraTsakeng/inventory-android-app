const { Pool } = require('pg');
require('dotenv').config();

const pool = new Pool({
host: 'localhost',
port: 5432,
database: 'inventoryapp_db',
user: 'postgres',
password: 'admin123'
});

pool.connect()
    .then(() => console.log('PostgreSQL connected'))
    .catch(err => console.error('PostgreSQL connection error:', err));

module.exports = pool;




























































































































































































